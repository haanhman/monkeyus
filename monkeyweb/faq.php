                        <a>Look for fireballs in the night sky as Taurid meteor swarm peaks</a>
                        <div class="icon"><i class="blog blog-faq-cong"></i></div>
                        <div class="clearfix"></div>
                    </div>
                    <div class="question">
                        <p>Earth takes a trip through a debris trail left by comet Encke every year. This happens around September through November and it can leave a lasting impression on eagle-eyed space fans in the form of the Taurid meteor shower, sometimes called the Taurid swarm.</p>
                        <p>This year is expected to be a particularly active year for Taurid meteors. They don't wow you with sheer numbers, but they can take the form of spectacular, long-lasting fireballs burning through the night sky. According to NASA, it takes only a pebble-size fragment to create a fantastic-looking fireball.</p>
                        <p>NASA shares some tips for spotting the fiery fun. The best viewing time is after midnight, and clear nights with no moonlight to spoil the show are your best bets. The first two weeks of November are prime time. If you see a bright streaking light, chances are good you just spotted a Taurid meteor.</p>
                        <p>The meteor shower gets its name because the burning objects appear to come from Taurus, the Bull constellation. If you see one, consider filing a report with the American Meteor Society, a group that encourages the study of meteors and tracks meteor activity. The society's fireball log also lets you check to see where other sightings are coming from and whether anyone else saw the same one you did.</p>
                        <p>Encke takes a trip around the sun every 3.3 years, which makes it a frequent flyer when it comes to comets near us. (Halley's comet only pops up about every 75 years.) Some years, the Taurid swarm is fairly quiet. Here's hoping the predictions for this year's activity come true and we're treated to some entertaining celestial pyrotechnics through the first half of the month.</p>
